# Variables
x = 10
y = 5

# Addition
result = x + y  
print("Addition (x + y):", result)

# Subtraction
result = x - y
print("Subtraction (x - y):", result)

# Multiplication
result = x * y
print("Multiplication (x * y):", result)

# Division
result = x / y
print("Division (x / y):", result)

# Modulus
result = x % y
print("Modulus (x % y):", result)

# Exponentiation
result = x ** y
print("Exponentiation (x ** y):", result)

# Floor Division
result = x // y
print("Floor Division (x // y):", result)
