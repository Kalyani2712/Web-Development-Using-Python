
# Find the Simple Interest on Rs. 200 for 5 years at 5% per year
# Input principal, rate, and time
principal = 200#float(input("Enter the principal amount: "))
rate = 5 #float(input("Enter the rate of interest per year: "))
time = 5#float(input("Enter the time in years: "))

# Calculate simple interest
simple_interest = (principal * rate * time) / 100

print("Simple Interest:", simple_interest)
